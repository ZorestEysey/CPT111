public class SimpleMath {
    public static void main(String[] args)  {
        int num_1=10;
        int num_2 = 3;
        System.out.println("num1 + num2 = "+(num_1+num_2));
        System.out.println("num1 / num2 = "+(num_1/num_2));
        double real_1=10.0;
        double real_2=3.0;
        System.out.println("real1 + real2 = "+ (real_1+real_2));
        System.out.println("real1 / real2 = "+ (real_1/real_2));
    }
}
